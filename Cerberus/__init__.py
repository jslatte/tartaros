class Cerberus():
    """ Library for handling the User Interface. """

    def __init__(self, logger):
        """
        INPUT
            logger: An initialized instance of a logging class to use.
        """

        # instantiate logger
        self.log = logger

        self.module_name = self.__class__.__name__
        self.log.info("Initializing %s module ..." % self.module_name)

    def run(self):

        # initialize instance of main window
        main = wx.App(redirect=False)
        MainWindow(self.log)
        main.MainLoop()